-- Database Bakau Lestari
CREATE DATABASE IF NOT EXISTS bakau_lestari;
USE bakau_lestari;

CREATE TABLE admin (
    id INT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL
);

